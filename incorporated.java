public class incorporated {
    String corpName;
    String corpDept;

    public incorporated(String CorpName, String CorpDept){
        this.corpName = CorpName;
        this.corpDept = CorpDept;
    }
    public String getCorpName(){
        return corpName;
    }
    public String getCorpDept(){
        return corpName;
    }
}
